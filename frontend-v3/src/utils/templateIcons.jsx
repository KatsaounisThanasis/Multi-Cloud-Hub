// Get icon for a template name
export const getTemplateIcon = (templateName) => {
  const normalizedName = templateName?.toLowerCase().replace(/[_\s]/g, '-') || '';
  return templateIcons[normalizedName] || templateIcons['default'];
};

// Get gradient colors based on template category
export const getTemplateGradient = (templateName) => {
  const name = templateName?.toLowerCase() || '';

  // Storage & Data - Blue
  if (name.includes('storage') || name.includes('bucket') || name.includes('sql') || name.includes('cosmos') || name.includes('database') || name.includes('bigquery')) {
    return { from: 'from-blue-500', to: 'to-cyan-500', shadow: 'shadow-blue-500/30', bg: 'from-blue-50 to-cyan-50' };
  }

  // Compute - Purple
  if (name.includes('vm') || name.includes('virtual-machine') || name.includes('compute') || name.includes('instance') || name.includes('scale-set')) {
    return { from: 'from-purple-500', to: 'to-indigo-500', shadow: 'shadow-purple-500/30', bg: 'from-purple-50 to-indigo-50' };
  }

  // Kubernetes & Containers - Teal
  if (name.includes('aks') || name.includes('gke') || name.includes('kubernetes') || name.includes('container') || name.includes('registry')) {
    return { from: 'from-teal-500', to: 'to-emerald-500', shadow: 'shadow-teal-500/30', bg: 'from-teal-50 to-emerald-50' };
  }

  // Serverless - Orange
  if (name.includes('function') || name.includes('cloud-run') || name.includes('serverless')) {
    return { from: 'from-orange-500', to: 'to-amber-500', shadow: 'shadow-orange-500/30', bg: 'from-orange-50 to-amber-50' };
  }

  // Networking - Green
  if (name.includes('network') || name.includes('vpc') || name.includes('load-balancer') || name.includes('gateway') || name.includes('ip')) {
    return { from: 'from-green-500', to: 'to-emerald-500', shadow: 'shadow-green-500/30', bg: 'from-green-50 to-emerald-50' };
  }

  // Security - Red
  if (name.includes('key-vault') || name.includes('secret') || name.includes('security') || name.includes('firewall')) {
    return { from: 'from-red-500', to: 'to-rose-500', shadow: 'shadow-red-500/30', bg: 'from-red-50 to-rose-50' };
  }

  // Caching - Pink
  if (name.includes('redis') || name.includes('cache') || name.includes('memory')) {
    return { from: 'from-pink-500', to: 'to-rose-500', shadow: 'shadow-pink-500/30', bg: 'from-pink-50 to-rose-50' };
  }

  // Messaging - Violet
  if (name.includes('service-bus') || name.includes('pubsub') || name.includes('queue') || name.includes('event')) {
    return { from: 'from-violet-500', to: 'to-purple-500', shadow: 'shadow-violet-500/30', bg: 'from-violet-50 to-purple-50' };
  }

  // Analytics - Indigo
  if (name.includes('analytics') || name.includes('log') || name.includes('monitor') || name.includes('data-factory') || name.includes('dataflow')) {
    return { from: 'from-indigo-500', to: 'to-blue-500', shadow: 'shadow-indigo-500/30', bg: 'from-indigo-50 to-blue-50' };
  }

  // API - Cyan
  if (name.includes('api')) {
    return { from: 'from-cyan-500', to: 'to-blue-500', shadow: 'shadow-cyan-500/30', bg: 'from-cyan-50 to-blue-50' };
  }

  // Default - Gray/Slate
  return { from: 'from-slate-500', to: 'to-gray-500', shadow: 'shadow-slate-500/30', bg: 'from-slate-50 to-gray-50' };
};

export default { templateIcons, getTemplateIcon, getTemplateGradient };
